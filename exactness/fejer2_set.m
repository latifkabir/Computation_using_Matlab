%    BIT Numerical Mathematics,
%    Volume 43, Number 1, 2003, pages 1-18.
%
%  Parameters:
%
%    Input, integer N, the order.  
%    N should be between 1 and 10.
%
%    Output, real X(N), the abscissas.
%
%    Output, real W(N), the weights.
%
  x = zeros ( n, 1 );
  w = zeros ( n, 1 );

  if ( n == 1 )

    x(1)   =  0.000000000000000;
    w(1) =  2.000000000000000;

  elseif ( n == 2 )

    x(1) =   -0.5000000000000000;
    x(2) =    0.5000000000000000;

    w(1) =  1.0000000000000000;
    w(2) =  1.0000000000000000;

  elseif ( n == 3 )

    x( 1) =  -0.7071067811865476;
    x( 2) =   0.0000000000000000;
    x( 3) =   0.7071067811865476;

    w(1) =  0.6666666666666666;
    w(2) =  0.6666666666666666;
    w(3) =  0.6666666666666666;

  elseif ( n == 4 )

