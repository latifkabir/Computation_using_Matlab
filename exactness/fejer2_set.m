    x( 4) =   0.2225209339563144;
    x( 5) =   0.6234898018587336;
    x( 6) =   0.9009688679024191;

    w( 1) = 0.2269152467244296;
    w( 2) = 0.3267938603769863;
    w( 3) = 0.4462908928985841;
    w( 4) = 0.4462908928985841;
    w( 5) = 0.3267938603769863;
    w( 6) = 0.2269152467244296;

  elseif ( n == 7 )

    x( 1) =  -0.9238795325112867;
    x( 2) =  -0.7071067811865476;
    x( 3) =  -0.3826834323650898;
    x( 4) =   0.0000000000000000;
    x( 5) =   0.3826834323650898;
    x( 6) =   0.7071067811865476;
    x( 7) =   0.9238795325112867;

    w( 1) = 0.1779646809620499;
    w( 2) = 0.2476190476190476;
    w( 3) = 0.3934638904665215;
    w( 4) = 0.3619047619047619;
    w( 5) = 0.3934638904665215;
    w( 6) = 0.2476190476190476;
    w( 7) = 0.1779646809620499;

  elseif ( n == 8 )

    x( 1) =  -0.9396926207859084;
    x( 2) =  -0.7660444431189780;
    x( 3) =  -0.5000000000000000;
    x( 4) =  -0.1736481776669304;
    x( 5) =   0.1736481776669304;
    x( 6) =   0.5000000000000000;
