    w( 4) = 0.4254644007500070;

  elseif ( n == 5 )

    x( 1) =  -0.8660254037844387;
    x( 2) =  -0.5000000000000000;
    x( 3) =   0.0000000000000000;
    x( 4) =   0.5000000000000000;
    x( 5) =   0.8660254037844387;

    w( 1) = 0.3111111111111111;
    w( 2) = 0.4000000000000000;
    w( 3) = 0.5777777777777777;
    w( 4) = 0.4000000000000000;
