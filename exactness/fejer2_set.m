    w( 6) = 0.2850073526699544;
    w( 7) = 0.2530509772156453;
    w( 8) = 0.2263866903636005;
    w( 9) = 0.1411354380109716;
    w(10) = 0.09441954173982806;

  else

    fprintf ( 1, '\n' );
