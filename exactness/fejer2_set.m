    w( 5) = 0.3395748964790348;
    w( 6) = 0.3142857142857143;
    w( 7) = 0.2063696457302284;
    w( 8) = 0.1397697435050225;

  elseif ( n == 9 )

    x( 1) =  -0.9510565162951535;
    x( 2) =  -0.8090169943749475;
    x( 3) =  -0.5877852522924731;
    x( 4) =  -0.3090169943749475;
    x( 5) =   0.0000000000000000;
    x( 6) =   0.3090169943749475;
    x( 7) =   0.5877852522924731;
    x( 8) =   0.8090169943749475;
    x( 9) =   0.9510565162951535;

    w( 1) = 0.1147810750857217;
